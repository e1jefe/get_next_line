#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*s;

	if (!size)
		return (0);
	s = malloc(sizeof(*s) * size);
	if (!s)
		return (0);
	s = ft_memset(s, 0, size);
	return (s);
}
